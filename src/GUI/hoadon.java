    public void showtablechitiethoadon(String mahoadon) {

        cthd.setColumnCount(0);
        cthd.setRowCount(0);
        cthd.addColumn("Mã Thuốc");// làm sao để biết được tên thuốc
        cthd.addColumn("Tên Thuốc");
        cthd.addColumn("Giá");
        cthd.addColumn("Số Lượng");
        cthd.addColumn("Thành Tiền");
        ArrayList<chitiethoadon_DTO> arr = new ArrayList<chitiethoadon_DTO>();
        //hoadon_BLL hdBLL=new hoadon_BLL();
        chitiethoadon_BLL cthd_BLL = new chitiethoadon_BLL();
        arr = cthd_BLL.theoma(mahoadon);
        for (int i = 0; i < arr.size(); i++) {
            chitiethoadon_DTO cthd_DTO = arr.get(i);
            String mahd = cthd_DTO.getMahd();
            String mathuoc = cthd_DTO.getMathuoc();
            String tenthuoc = cthd_BLL.gettenthuoc(mathuoc);
            double gia = cthd_BLL.getgia(mathuoc);
            int sl = cthd_DTO.getSl();

            double thanhtien = gia * sl;
            tongtienthuoc += thanhtien;
            Object[] obj = {mathuoc, tenthuoc, gia, sl, thanhtien};
            cthd.addRow(obj);
        }
        tablechitiethoadon.setModel(cthd);
    }

