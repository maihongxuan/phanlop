    private void btnthemthuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemthuocActionPerformed
        // TODO add your handling code here:

        thuoc_DTO thuoc = new thuoc_DTO();
        thuoc_BLL thuoc_BLL = new thuoc_BLL();
        String tenthuoc = String.valueOf(hoadoncbbtt.getSelectedItem());
        thuoc.setTenthuoc(tenthuoc);
        String mathuoc = thuoc_BLL.getmathuoc(tenthuoc);
        for (int i = 0; i < cthd.getRowCount(); i++) {
            String tmp = (String) cthd.getValueAt(i, 0);

            if (mathuoc.equals(tmp.trim())) {
                JOptionPane.showConfirmDialog(this, "Mã thuốc đã được thêm vào bảng!!!", "Thông báo", JOptionPane.CLOSED_OPTION);
                return;
            }
        }
        thuoc.setMathuoc(mathuoc);
        double giathuoc = Double.parseDouble(tfgiathuoc.getText());
        thuoc.setGia(giathuoc);
        int sl = (int) tfslthuoc.getValue();
        if (thuoc_BLL.kiemtrathuoc(mathuoc, sl) == false) {
            JOptionPane.showConfirmDialog(this, "Kho thuốc không đủ số lượng yêu cầu !!", "Thông báo", JOptionPane.CLOSED_OPTION);
            return;
        }
        thuoc.setSl(sl);

        double thanhtien = giathuoc * sl;
        Object[] obj = {mathuoc, tenthuoc, giathuoc, sl, thanhtien};
        cthd.addRow(obj);
        tablechitiethoadon.setModel(cthd);
        for (int i = 0; i < cthd.getRowCount(); i++) {
            tongtienthuoc += (double) cthd.getValueAt(i, 4);

        }
        lbtongtienthuoc.setText("Tiền thuốc : " + tongtienthuoc);

        lbthanhtien.setText("Tổng Cộng : " + (tongtienthuoc + tongtiendv));

    }//GEN-LAST:event_btnthemthuocActionPerformed

